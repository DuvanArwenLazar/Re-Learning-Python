# Boolean operators are use for compare boolean values.

# 1. Not: Changes a True for a False and Changes a False for a True.
print(not(True)) # False.

# 2. And: Compares two boolean values. If both are True, "And" will be True. Else, "And" will be False.
print(True and False) # False.

# 3. Or: Compares two booleans values. Only needs one True value for will be True. If both bool values are False, "Or" will be False.
print(True or False) # True.