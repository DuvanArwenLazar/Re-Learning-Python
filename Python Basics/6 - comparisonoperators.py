# Those operators returns a boolean value.

# 1. Equal:
print(5 == 3) # False.

# 2. Greater than:
print(5 > 3) # True.

# 3. Less than.
print(5 < 5) # False.

# 4. Grater than or equal:
print(10 >= 10) # True.

# 5. Less than or equal:
print(4 <= 10) # True.

# 6. Diferent than:
print(5 != 10) # True.
# We'll use this operators for if statements and cicles.

